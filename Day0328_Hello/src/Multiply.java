
public class Multiply {

	public static void main(String[] args) {
		
		int q; //변수 선언, 'q'라는 이름의 정수형 변수 선언
		int w; //변수 선언, 'w'라는 이름의 정수형 변수 선언
		int e; //변수 선언, 'e'라는 이름의 정수형 변수 선언
		
		q = 50; //q라는 변수에 50을 넣어줌
		w = 10; //q라는 변수에 10을 넣어줌
		e = q * w; // e라는 변수에 q * w을 넣어줌

		System.out.print("q와w의곱은?"+e+"입니다.");
		System.out.println("q와w의곱은?");//""안의 내용을 콘솔에 출력
		System.out.println(e); //e이라는 변수가 가지고있는 값을 콘솔에 출력
		System.out.println("입니다.");
	}
}
